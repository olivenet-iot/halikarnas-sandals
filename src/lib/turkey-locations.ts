// Helper functions
export function getCityById(id: string): City | undefined {
  return cities.find((city) => city.id === id);
}

export function getCityByPlateCode(plateCode: string): City | undefined {
  return cities.find((city) => city.plateCode === plateCode);
}

export function getCityByName(name: string): City | undefined {
  return cities.find(
    (city) => city.name.toLowerCase() === name.toLowerCase()
  );
}

export function getDistrictsByCityId(cityId: string): District[] {
  return districts.filter((district) => district.cityId === cityId);
}

export function getDistrictById(id: string): District | undefined {
  return districts.find((district) => district.id === id);
}

export function getDistrictByName(
  cityId: string,
  name: string
): District | undefined {
  return districts.find(
    (district) =>
      district.cityId === cityId &&
      district.name.toLowerCase() === name.toLowerCase()
  );
}

// Get all cities sorted alphabetically
export function getCitiesSorted(): City[] {
  return [...cities].sort((a, b) => a.name.localeCompare(b.name, "tr"));
}

// Get districts sorted alphabetically
export function getDistrictsSorted(cityId: string): District[] {
  return getDistrictsByCityId(cityId).sort((a, b) =>
    a.name.localeCompare(b.name, "tr")
  );
}
